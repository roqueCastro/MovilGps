public class Pregunta {
    private  Integer id_pre;
    private  String nombre_pre;
    private  Integer tipo_pre;

    public Integer getId_pre() {
        return id_pre;
    }

    public void setId_pre(Integer id_pre) {
        this.id_pre = id_pre;
    }

    public String getNombre_pre() {
        return nombre_pre;
    }

    public void setNombre_pre(String nombre_pre) {
        this.nombre_pre = nombre_pre;
    }

    public Integer getTipo_pre() {
        return tipo_pre;
    }

    public void setTipo_pre(Integer tipo_pre) {
        this.tipo_pre = tipo_pre;
    }
}
